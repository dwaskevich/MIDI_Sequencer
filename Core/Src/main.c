static void midiSend(uint8_t status, uint8_t note, uint8_t channel, uint8_t velocity)
{
    if (channel < 1 || channel > 16)
    	channel = 1;
    channel -= 1;
    uint8_t packet[3] = {
        (status & 0xF0) | (channel & 0x0F),
        note & 0x7F,
        velocity & 0x7F};
    HAL_UART_Transmit(&huart2, packet, sizeof(packet), 100);
}

void midiSendNoteOn(uint8_t note, uint8_t channel, uint8_t velocity) {
    midiSend(0x90, note, channel, velocity);
}

void midiSendNoteOff(uint8_t note, uint8_t channel, uint8_t velocity) {
    midiSend(0x80, note, channel, velocity);
}

uint16_t randomize(uint16_t lower_value, uint16_t upper_value)
{
	return rand() % (upper_value - lower_value + 1) + lower_value;
}

